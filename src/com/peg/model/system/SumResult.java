package com.peg.model.system;

public class SumResult {
	private String installDate;
	private String installAmount;
	private String finishDate;
	private String repairAmount;
	public String getInstallDate()
	{
		return installDate;
	}
	public void setInstallDate(String installDate)
	{
		this.installDate = installDate;
	}
	public String getInstallAmount()
	{
		return installAmount;
	}
	public void setInstallAmount(String installAmount)
	{
		this.installAmount = installAmount;
	}
	public String getFinishDate()
	{
		return finishDate;
	}
	public void setFinishDate(String finishDate)
	{
		this.finishDate = finishDate;
	}
	public String getRepairAmount()
	{
		return repairAmount;
	}
	public void setRepairAmount(String repairAmount)
	{
		this.repairAmount = repairAmount;
	}
	
	
}
