package com.peg.model;

public class SupplierRef {
	
	private Long id;
	
	private String supplierNumber;
	
	private String supplierNumberN;
	
	private String supplierName;
	
	private String supplierNameN;
	
	private String supplierShortName;
	
	private String partNumber;
	
	private String partName;
	
	private String supplierCode;

	public Long getId() {
		return id;
	}
	
	public void setId(Long id) {
		this.id = id;
	}
	
	public String getSupplierNumber() {
		return supplierNumber;
	}

	public void setSupplierNumber(String supplierNumber) {
		this.supplierNumber = supplierNumber;
	}

	public String getSupplierNumberN() {
		return supplierNumberN;
	}

	public void setSupplierNumberN(String supplierNumberN) {
		this.supplierNumberN = supplierNumberN;
	}

	public String getSupplierName() {
		return supplierName;
	}

	public void setSupplierName(String supplierName) {
		this.supplierName = supplierName;
	}

	public String getSupplierNameN() {
		return supplierNameN;
	}

	public void setSupplierNameN(String supplierNameN) {
		this.supplierNameN = supplierNameN;
	}

	public String getSupplierShortName() {
		return supplierShortName;
	}

	public void setSupplierShortName(String supplierShortName) {
		this.supplierShortName = supplierShortName;
	}
	
	public String getPartNumber() {
		return partNumber;
	}

	public void setPartNumber(String partNumber) {
		this.partNumber = partNumber;
	}

	public String getPartName() {
		return partName;
	}

	public void setPartName(String partName) {
		this.partName = partName;
	}

	public String getSupplierCode() {
		return supplierCode;
	}

	public void setSupplierCode(String supplierCode) {
		this.supplierCode = supplierCode;
	}
}
